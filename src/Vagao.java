
public class Vagao {
	
	    private char identificador;
	    private String carga;

	    public Vagao(char identificador, String carga) {
	        this.identificador = identificador;
	        this.carga = carga;
	    }

	    public char getIdentificador() {
	        return identificador;
	    }

	    public String getCarga() {
	        return carga;
	    }
	}

